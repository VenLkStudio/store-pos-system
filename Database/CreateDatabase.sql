-- =============================================
-- База данных: Розничный магазин
-- =============================================

USE master;
GO

IF EXISTS (SELECT name FROM sys.databases WHERE name = 'RetailShop')
    DROP DATABASE RetailShop;
GO

CREATE DATABASE RetailShop;
GO

USE RetailShop;
GO

-- =============================================
-- Таблица: Поставщики
-- =============================================
CREATE TABLE Поставщики (
    id INT IDENTITY(1,1) PRIMARY KEY,
    название NVARCHAR(200) NOT NULL,
    ИНН NVARCHAR(12),
    контакт NVARCHAR(200),
    телефон NVARCHAR(20),
    адрес NVARCHAR(300)
);

-- =============================================
-- Таблица: Сотрудники
-- =============================================
CREATE TABLE Сотрудники (
    id INT IDENTITY(1,1) PRIMARY KEY,
    ФИО NVARCHAR(200) NOT NULL,
    должность NVARCHAR(100) NOT NULL,
    логин NVARCHAR(50) UNIQUE NOT NULL,
    пароль NVARCHAR(100) NOT NULL,
    роль NVARCHAR(50) NOT NULL -- Оператор, Администратор, Товаровед, СтаршийКассир
);

-- =============================================
-- Таблица: Товары
-- =============================================
CREATE TABLE Товары (
    id INT IDENTITY(1,1) PRIMARY KEY,
    название NVARCHAR(200) NOT NULL,
    ашС NVARCHAR(50),
    единицаИзмерения NVARCHAR(20) DEFAULT 'шт'
);

-- =============================================
-- Таблица: Склад
-- =============================================
CREATE TABLE Склад (
    id INT IDENTITY(1,1) PRIMARY KEY,
    товарId INT NOT NULL REFERENCES Товары(id),
    количество INT NOT NULL DEFAULT 0,
    адрес NVARCHAR(100)
);

-- =============================================
-- Таблица: РозничныеЦены
-- =============================================
CREATE TABLE РозничныеЦены (
    id INT IDENTITY(1,1) PRIMARY KEY,
    товарId INT NOT NULL REFERENCES Товары(id),
    закупочнаяЦена DECIMAL(10,2) NOT NULL,
    розничнаяЦена DECIMAL(10,2) NOT NULL,
    наценка DECIMAL(5,2),
    датаУстановки DATETIME DEFAULT GETDATE()
);

-- =============================================
-- Таблица: ПартииТовара
-- =============================================
CREATE TABLE ПартииТовара (
    id INT IDENTITY(1,1) PRIMARY KEY,
    датаПоступления DATETIME NOT NULL DEFAULT GETDATE(),
    поставщикId INT NOT NULL REFERENCES Поставщики(id),
    операторId INT NOT NULL REFERENCES Сотрудники(id),
    статус NVARCHAR(50) DEFAULT 'Принята' -- Принята, Отклонена, Частично принята
);

-- =============================================
-- Таблица: СтрокиПартии
-- =============================================
CREATE TABLE СтрокиПартии (
    id INT IDENTITY(1,1) PRIMARY KEY,
    партияId INT NOT NULL REFERENCES ПартииТовара(id),
    товарId INT NOT NULL REFERENCES Товары(id),
    количество INT NOT NULL,
    цена DECIMAL(10,2) NOT NULL
);

-- =============================================
-- Таблица: Документы (Накладная, СчетФактура, Сертификат)
-- =============================================
CREATE TABLE Документы (
    id INT IDENTITY(1,1) PRIMARY KEY,
    партияId INT NOT NULL REFERENCES ПартииТовара(id),
    тип NVARCHAR(50) NOT NULL, -- Накладная, СчетФактура, СертификатКачества
    номер NVARCHAR(50),
    дата DATE,
    сумма DECIMAL(10,2),
    проверен BIT DEFAULT 0
);

-- =============================================
-- Таблица: ЗаявкиВТорговыйЗал
-- =============================================
CREATE TABLE ЗаявкиВТорговыйЗал (
    id INT IDENTITY(1,1) PRIMARY KEY,
    товарId INT NOT NULL REFERENCES Товары(id),
    количество INT NOT NULL,
    администраторId INT NOT NULL REFERENCES Сотрудники(id),
    датаЗаявки DATETIME DEFAULT GETDATE(),
    статус NVARCHAR(50) DEFAULT 'Новая' -- Новая, Выполнена
);

-- =============================================
-- Таблица: ВозвратныеНакладные
-- =============================================
CREATE TABLE ВозвратныеНакладные (
    id INT IDENTITY(1,1) PRIMARY KEY,
    партияId INT NOT NULL REFERENCES ПартииТовара(id),
    товаровЕдId INT NOT NULL REFERENCES Сотрудники(id),
    причинаВозврата NVARCHAR(500),
    количество INT NOT NULL,
    товарId INT NOT NULL REFERENCES Товары(id),
    дата DATETIME DEFAULT GETDATE(),
    статус NVARCHAR(50) DEFAULT 'Оформлена'
);

-- =============================================
-- Таблица: Кассы
-- =============================================
CREATE TABLE Кассы (
    id INT IDENTITY(1,1) PRIMARY KEY,
    номер NVARCHAR(20) NOT NULL,
    статус NVARCHAR(20) DEFAULT 'Закрыта', -- Открыта, Закрыта
    кассирId INT REFERENCES Сотрудники(id),
    кассирПолId INT REFERENCES Сотрудники(id) -- полная ссылка на старший кассир
);

-- =============================================
-- Таблица: Чеки
-- =============================================
CREATE TABLE Чеки (
    id INT IDENTITY(1,1) PRIMARY KEY,
    дата DATETIME DEFAULT GETDATE(),
    сумма DECIMAL(10,2) NOT NULL,
    итого DECIMAL(10,2) NOT NULL,
    кассаId INT NOT NULL REFERENCES Кассы(id),
    скидка DECIMAL(5,2) DEFAULT 0,
    аннулирован BIT DEFAULT 0
);

-- =============================================
-- Таблица: СтрокиЧека
-- =============================================
CREATE TABLE СтрокиЧека (
    id INT IDENTITY(1,1) PRIMARY KEY,
    чекId INT NOT NULL REFERENCES Чеки(id),
    товарId INT NOT NULL REFERENCES Товары(id),
    количество INT NOT NULL,
    цена DECIMAL(10,2) NOT NULL
);

-- =============================================
-- Таблица: РеализацияТоваров
-- =============================================
CREATE TABLE РеализацияТоваров (
    id INT IDENTITY(1,1) PRIMARY KEY,
    чекId INT NOT NULL REFERENCES Чеки(id),
    товарId INT NOT NULL REFERENCES Товары(id),
    дата DATETIME DEFAULT GETDATE(),
    кассаId INT NOT NULL REFERENCES Кассы(id),
    сумма DECIMAL(10,2),
    количество INT
);

-- =============================================
-- Таблица: КассовыеОтчеты
-- =============================================
CREATE TABLE КассовыеОтчеты (
    id INT IDENTITY(1,1) PRIMARY KEY,
    кассаId INT NOT NULL REFERENCES Кассы(id),
    старшийКассирId INT NOT NULL REFERENCES Сотрудники(id),
    дата DATE NOT NULL DEFAULT CAST(GETDATE() AS DATE),
    итоговаяСумма DECIMAL(10,2),
    количествоЧеков INT
);

-- =============================================
-- Таблица: Инвентаризация
-- =============================================
CREATE TABLE Инвентаризация (
    id INT IDENTITY(1,1) PRIMARY KEY,
    дата DATETIME DEFAULT GETDATE(),
    ответственный NVARCHAR(200),
    фактОстаток INT,
    базаОстаток INT,
    статус NVARCHAR(50) DEFAULT 'В процессе'
);

-- =============================================
-- Таблица: СтрокиИнвентаризации
-- =============================================
CREATE TABLE СтрокиИнвентаризации (
    id INT IDENTITY(1,1) PRIMARY KEY,
    инвентаризацияId INT NOT NULL REFERENCES Инвентаризация(id),
    товарId INT NOT NULL REFERENCES Товары(id),
    фактОстаток INT NOT NULL,
    базаОстаток INT NOT NULL
);

